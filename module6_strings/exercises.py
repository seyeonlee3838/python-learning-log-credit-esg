# Module 6: String Handling Examples

# 1. Looping through a string
fruit = "banana"
for letter in fruit:
    print(letter)

# 2. Slicing and searching
text = "Hello World"
print("Slice:", text[0:5])
print("Find 'World':", text.find("World"))

# 3. Using string methods
greet = "  hello python  "
print("Upper:", greet.upper())
print("Stripped:", greet.strip())
